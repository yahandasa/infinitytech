async function downloadAllAsZip(project: NonNullable<ReturnType<typeof useProject>["data"]>) {
  const JSZip = (await import("jszip")).default;
  const zip = new JSZip();
  const folder = zip.folder(project.id) as InstanceType<typeof JSZip>;

  for (const file of project.files) {
    if (file.url) {
      try {
        const res = await fetch(file.url);
        const blob = await res.blob();
        folder.file(file.name, blob);
      } catch {
        folder.file(file.name, `File: ${file.name}\nStatus: Download from project source.`);
      }
    } else {
      folder.file(
        file.name,
        `Project: ${project.title}\nFile: ${file.name}\nType: ${file.type}\nDescription: ${file.description}\n\nContact to obtain this file.`
      );
    }
  }

  folder.file(
    "README.txt",
    `${project.title}\n${"=".repeat(project.title.length)}\n\n${project.description}\n\nGitHub: ${project.githubUrl}\n\nGenerated by INFINITY.TECH`
  );

  const blob = await zip.generateAsync({ type: "blob" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = `${project.id}-files.zip`;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

